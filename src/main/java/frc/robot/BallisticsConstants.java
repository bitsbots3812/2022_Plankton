package frc.robot;

public class BallisticsConstants {
    double limelightMountHeight, limelightMountAngle, targetHeight, targetRadius, targetDepth;

    BallisticsConstants (double MountHeight, double MountAngle, double TargetHeight, double TargetRadius, double TargetDepth) {
        limelightMountHeight = MountHeight;
        limelightMountAngle = MountAngle;
        targetHeight = TargetHeight;
        targetRadius = TargetRadius;
        targetDepth = TargetDepth;
    }
}
